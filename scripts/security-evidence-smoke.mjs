#!/usr/bin/env node
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import { pathToFileURL } from 'node:url';
import { recordGateCommandEvidence } from './evidence-recorder.mjs';
import { runRedactionPathSafetyEvidence } from './redaction-path-safety-evidence.mjs';
import { runReleaseGateCommand, spawnProcess } from './release-gate-runner.mjs';
import { EXPECTED_EVIDENCE, RELEASE_GATE_EXECUTIONS, RELEASE_TIERS } from './release-gates.mjs';

function parseArgs(argv) {
  const options = {
    evidenceDir: undefined,
    keep: false,
    mockSecurity: false,
  };

  for (let index = 0; index < argv.length; index++) {
    const arg = argv[index];
    if (arg === '--') {
      continue;
    } else if (arg === '--evidence-dir') {
      options.evidenceDir = argv[++index];
    } else if (arg?.startsWith('--evidence-dir=')) {
      options.evidenceDir = arg.slice('--evidence-dir='.length);
    } else if (arg === '--keep') {
      options.keep = true;
    } else if (arg === '--mock-security') {
      options.mockSecurity = true;
    } else {
      throw new Error(`Unknown argument: ${arg}`);
    }
  }

  return options;
}

function tierIncluded(entryTier, requiredTier) {
  return RELEASE_TIERS.indexOf(entryTier) <= RELEASE_TIERS.indexOf(requiredTier);
}

function timestamp() {
  return new Date().toISOString();
}

function writePlaceholderArtifact(evidenceDir, entry) {
  const artifactPath = path.join(evidenceDir, entry.filename);
  fs.mkdirSync(path.dirname(artifactPath), { recursive: true });
  fs.writeFileSync(
    artifactPath,
    `placeholder: ${entry.name}\ncommand: ${entry.command}\nreason: generated by security evidence smoke\n`,
  );
}

function recordPlaceholderCommandEvidence(evidenceDir, gate) {
  const startedAt = timestamp();
  recordGateCommandEvidence(
    {
      name: gate.name,
      command: gate.command,
      filename: gate.filename,
      stdout: `placeholder pass for ${gate.name}\n`,
      stderr: '',
      exitCode: 0,
      startedAt,
      finishedAt: timestamp(),
    },
    { evidenceDir },
  );
}

function requiredRcEvidence() {
  return EXPECTED_EVIDENCE.filter((entry) => tierIncluded(entry.tier, 'rc') && !isDesktopEvidence(entry));
}

function isDesktopEvidence(entry) {
  return entry.name?.startsWith('desktop-') || entry.command?.startsWith('pnpm desktop:') || entry.command === 'pnpm rc:desktop-gate';
}

async function recordSecurityGate(evidenceDir, options) {
  const securityGate = EXPECTED_EVIDENCE.find((entry) => entry.name === 'security-regression');
  if (!securityGate) {
    throw new Error('security-regression evidence gate is not registered');
  }

  if (options.mockSecurity) {
    recordPlaceholderCommandEvidence(evidenceDir, securityGate);
    return;
  }

  const result = await runReleaseGateCommand(securityGate, {
    recordEvidence: true,
    evidenceDir,
  });
  if (result.exitCode !== 0) {
    throw new Error(`Security regression smoke failed with exit code ${result.exitCode}`);
  }
}

async function recordRedactionPathSafetyEvidence(evidenceDir) {
  await runRedactionPathSafetyEvidence({
    evidenceDir,
    skipTests: true,
  });
}

async function generateManifest(evidenceDir) {
  const result = await spawnProcess({
    file: process.execPath,
    args: [
      path.join('scripts', 'evidence-manifest.mjs'),
      '--evidence-dir',
      evidenceDir,
      '--output',
      path.join(evidenceDir, 'evidence-manifest.json'),
      '--require=rc',
      '--surface=cli-mcp-github',
    ],
    cwd: process.cwd(),
    env: process.env,
  });

  if (result.exitCode !== 0) {
    throw new Error(
      [
        `Evidence manifest smoke failed with exit code ${result.exitCode}`,
        result.stdout,
        result.stderr,
      ].filter(Boolean).join('\n'),
    );
  }
}

function assertSecurityCaptured(evidenceDir) {
  const manifestPath = path.join(evidenceDir, 'evidence-manifest.json');
  const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf-8'));
  const securityEntry = manifest.entries.find((entry) => entry.name === 'security-regression');
  const redactionPathSafetyEntry = manifest.entries.find((entry) => entry.name === 'redaction-path-safety-evidence');

  if (!securityEntry) {
    throw new Error('security-regression entry is missing from the evidence manifest');
  }
  if (!redactionPathSafetyEntry) {
    throw new Error('redaction-path-safety-evidence entry is missing from the evidence manifest');
  }
  if (securityEntry.command !== 'pnpm test:security') {
    throw new Error(`security-regression command drifted: ${securityEntry.command}`);
  }
  if (securityEntry.tier !== 'rc') {
    throw new Error(`security-regression tier drifted: ${securityEntry.tier}`);
  }
  if (securityEntry.latestCommandEvidence?.command !== 'pnpm test:security') {
    throw new Error('security-regression latestCommandEvidence is missing pnpm test:security');
  }
  if (securityEntry.latestCommandEvidence.exitCode !== 0) {
    throw new Error(`security-regression exit code was ${securityEntry.latestCommandEvidence.exitCode}`);
  }
  if (redactionPathSafetyEntry.command !== 'pnpm evidence:redaction-path-safety') {
    throw new Error(`redaction-path-safety-evidence command drifted: ${redactionPathSafetyEntry.command}`);
  }
  if (redactionPathSafetyEntry.tier !== 'rc') {
    throw new Error(`redaction-path-safety-evidence tier drifted: ${redactionPathSafetyEntry.tier}`);
  }
  if (redactionPathSafetyEntry.exists !== true) {
    throw new Error('redaction-path-safety-evidence artifact is missing from the rc manifest');
  }
  const redactionPathSafetyArtifact = JSON.parse(fs.readFileSync(path.join(evidenceDir, redactionPathSafetyEntry.filename), 'utf-8'));
  if (redactionPathSafetyArtifact.schemaVersion !== 'codeagora.redaction-path-safety-evidence.v1') {
    throw new Error(`redaction-path-safety-evidence schema drifted: ${redactionPathSafetyArtifact.schemaVersion}`);
  }
  if (redactionPathSafetyArtifact.tests?.passed !== true) {
    throw new Error('redaction-path-safety-evidence tests were not captured as passed');
  }
  if (redactionPathSafetyArtifact.checks?.persistedSessionArtifactRedaction !== true) {
    throw new Error('redaction-path-safety-evidence did not capture persisted artifact redaction');
  }
  if (redactionPathSafetyArtifact.checks?.symlinkEscapesRejected !== true) {
    throw new Error('redaction-path-safety-evidence did not capture symlink path safety');
  }
  if (manifest.gateSummary?.passed !== true) {
    throw new Error('rc evidence manifest gate summary did not pass');
  }

  return manifestPath;
}

async function run(options) {
  const evidenceDir = path.resolve(
    options.evidenceDir ?? fs.mkdtempSync(path.join(os.tmpdir(), 'codeagora-security-evidence-smoke-')),
  );
  fs.mkdirSync(evidenceDir, { recursive: true });

  try {
    for (const entry of requiredRcEvidence()) {
      if (
        entry.name === 'security-regression'
        || entry.name === 'redaction-path-safety-evidence'
      ) {
        continue;
      }
      if (entry.execution === RELEASE_GATE_EXECUTIONS.LOCAL_COMMAND) {
        recordPlaceholderCommandEvidence(evidenceDir, entry);
      } else {
        writePlaceholderArtifact(evidenceDir, entry);
      }
    }

    await recordSecurityGate(evidenceDir, options);
    await recordRedactionPathSafetyEvidence(evidenceDir);
    await generateManifest(evidenceDir);
    const manifestPath = assertSecurityCaptured(evidenceDir);
    console.log(`Security evidence smoke passed: ${path.relative(process.cwd(), manifestPath)}`);
  } finally {
    if (!options.keep && !options.evidenceDir) {
      fs.rmSync(evidenceDir, { recursive: true, force: true });
    }
  }
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  try {
    await run(parseArgs(process.argv.slice(2)));
  } catch (error) {
    console.error(error instanceof Error ? error.message : String(error));
    process.exit(1);
  }
}
